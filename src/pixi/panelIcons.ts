/** Resolve the tint colour for a panel icon from the active theme. */
export function getIconColor(key: string, theme: GameThemeColors): string {
  switch (key) {
    case "hearts":
      return theme.life;
    case "poison":
      return theme.poison;
    case "energy":
      return theme.badges.energy;
    case "hand":
      return theme.badges.hand;
    case "barbute":
      return theme.textGhost;
    case "deck":
    case "graveyard":
    case "exile":
    case "cmdsword":
    default:
      return theme.counter.default;
  }
}

interface PendingSprite {
  sprite: Sprite;
  displayWidth?: number;
  displayHeight?: number;
}

const iconCache = new Map<string, Texture>();
const pendingSprites = new Map<string, Set<PendingSprite>>();

export const ICON_RASTER = 64;
export const PANEL_ICON_VIEWBOX = 512;

/** The single icon-precedence site: the hand-picked `SVG` registry wins over
 *  the iconify game-icons pack, so a zone glyph renders identically in the
 *  Pixi texture cache and the DOM `GameIcon`. */
export function resolveIconBody(
  name: string,
): { body: string; width: number; height: number } | null {
  const body = SVG[name];
  if (body) return { body, width: PANEL_ICON_VIEWBOX, height: PANEL_ICON_VIEWBOX };
  const icon = gameIconsPack.icons[name];
  if (!icon) return null;
  return {
    body: icon.body,
    width: icon.width ?? gameIconsPack.width ?? PANEL_ICON_VIEWBOX,
    height: icon.height ?? gameIconsPack.height ?? PANEL_ICON_VIEWBOX,
  };
}

function getIconBody(key: string): string | undefined {
  if (SVG[key]) return SVG[key];
  const icon = gameIconsPack.icons[key];
  if (!icon) return undefined;
  return icon.body;
}

export function rasterIcon(key: string, hex: string, size: number): void {
  const cacheKey = `${key}:${hex}:${size}`;
  if (iconCache.has(cacheKey)) return;
  const body = getIconBody(key);
  if (!body) return;
  const coloured = body.replaceAll("currentColor", hex);
  const svg = `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 512 512" width="${size}" height="${size}">${coloured}</svg>`;
  const blob = new Blob([svg], { type: "image/svg+xml" });
  const url = URL.createObjectURL(blob);
  const img = new Image();
  img.width = size;
  img.height = size;
  img.src = url;
  img.onload = () => {
    const c = document.createElement("canvas");
    c.width = size;
    c.height = size;
    c.getContext("2d")!.drawImage(img, 0, 0, size, size);
    URL.revokeObjectURL(url);
    const tex = Texture.from(c);
    iconCache.set(cacheKey, tex);
    pendingSprites.get(cacheKey)?.forEach((entry) => {
      if (entry.sprite.destroyed) return;
      entry.sprite.texture = tex;
      if (entry.displayWidth != null) entry.sprite.width = entry.displayWidth;
      if (entry.displayHeight != null) entry.sprite.height = entry.displayHeight;
    });
    pendingSprites.delete(cacheKey);
  };
}

export function applyIcon(
  sprite: Sprite,
  key: string,
  hex: string,
  size = ICON_RASTER,
  displayWidth?: number,
  displayHeight?: number,
): void {
  const cacheKey = `${key}:${hex}:${size}`;
  const cached = iconCache.get(cacheKey);
  if (cached) {
    sprite.texture = cached;
    if (displayWidth != null) sprite.width = displayWidth;
    if (displayHeight != null) sprite.height = displayHeight;
    return;
  }
  rasterIcon(key, hex, size);
  if (!pendingSprites.has(cacheKey)) pendingSprites.set(cacheKey, new Set());
  pendingSprites.get(cacheKey)!.add({ sprite, displayWidth, displayHeight });
}
