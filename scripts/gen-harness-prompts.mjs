// Generates typed Java classes for the harness's prompt emission, from the
// protocol's generated TS bindings (the same source `ts-rs` produces from the
// Rust types). Each prompt-input class bakes in its own `type` discriminator,
// so the harness physically cannot build a prompt with the wrong type, an
// unknown field, or a wrong field type — it won't compile.
//
// Emission side only: the *Input structs and the value types they reference,
// transitively. *Output types (the response direction) are not generated.
//
// Usage: node scripts/gen-harness-prompts.mjs <output-dir>
import { readFileSync, readdirSync, statSync, mkdirSync, writeFileSync, rmSync } from "node:fs";
import { join } from "node:path";

const root = process.cwd();
const PROTO = join(root, "src/protocol");
const PKG = "forge.harness.protocol";
const outRoot = process.argv[2] ?? join(root, "forge-harness/target/generated-sources/protocol");
const outDir = join(outRoot, ...PKG.split("."));

const walk = (dir) => {
  const out = [];
  for (const e of readdirSync(dir)) {
    const p = join(dir, e);
    if (statSync(p).isDirectory()) out.push(...walk(p));
    else if (e.endsWith(".ts")) out.push(p);
  }
  return out;
};

// ---- parse every generated alias -------------------------------------------

const aliases = new Map(); // Name -> raw body (between `= ` and `;`)
for (const file of walk(PROTO))
  for (const line of readFileSync(file, "utf8").split("\n")) {
    const m = line.match(/^export type (\w+) = (.+);$/);
    if (m) aliases.set(m[1], m[2].trim());
  }

// Input type -> its PromptInput tag, e.g. ChooseFromSelectionInput -> "chooseFromSelection".
const inputTag = new Map();
for (const m of (aliases.get("PromptInput") ?? "").matchAll(/\{ "type": "(\w+)" \}(?: & (\w+))?/g))
  if (m[2]) inputTag.set(m[2], m[1]);

const splitTopLevel = (s, sep) => {
  const out = [];
  let depth = 0;
  let cur = "";
  for (let i = 0; i < s.length; i++) {
    const c = s[i];
    if (c === "{" || c === "<") depth++;
    else if (c === "}" || c === ">") depth--;
    if (depth === 0 && s.startsWith(sep, i)) {
      out.push(cur);
      cur = "";
      i += sep.length - 1;
    } else cur += c;
  }
  if (cur.trim()) out.push(cur);
  return out.map((x) => x.trim());
};

const parseFields = (objBody) => {
  const inner = objBody.replace(/^\{/, "").replace(/\}$/, "").trim();
  const fields = [];
  let discriminator = null;
  for (const part of splitTopLevel(inner, ",")) {
    if (!part) continue;
    const disc = part.match(/^"(\w+)": "(\w+)"$/);
    if (disc) {
      discriminator = { key: disc[1], value: disc[2] };
      continue;
    }
    const fm = part.match(/^(\w+)(\?)?:\s*(.+)$/);
    if (fm) fields.push({ name: fm[1], optional: !!fm[2], ts: fm[3].trim() });
  }
  return { discriminator, fields };
};

// type names referenced by a body (capitalized identifiers that are aliases)
const refsOf = (body) => {
  const out = new Set();
  for (const m of body.matchAll(/\b([A-Z]\w+)\b/g)) if (aliases.has(m[1])) out.add(m[1]);
  return out;
};

// ---- reachable closure from the input types --------------------------------

const reachable = new Set();
const queue = [...inputTag.keys()];
while (queue.length) {
  const n = queue.pop();
  if (reachable.has(n) || !aliases.has(n) || /Output$/.test(n)) continue;
  reachable.add(n);
  for (const r of refsOf(aliases.get(n))) queue.push(r);
}

// ---- TS type -> Java type ---------------------------------------------------

const javaType = (ts, optional) => {
  ts = ts.trim();
  const nullable = / \| null$/.test(ts);
  if (nullable) ts = ts.replace(/ \| null$/, "").trim();
  const opt = optional || nullable;
  const map = ts.match(/^\{ \[key in string\]\??: (.+) \}$/);
  if (map) return `java.util.Map<String, ${javaType(map[1], true)}>`;
  const record = ts.match(/^Record<string, (.+)>$/);
  if (record) return `java.util.Map<String, ${javaType(record[1], true)}>`;
  const arr = ts.match(/^Array<(.+)>$/);
  if (arr) return `java.util.List<${javaType(arr[1], true)}>`;
  switch (ts) {
    case "string":
      return "String";
    case "number":
      return opt ? "Integer" : "int";
    case "boolean":
      return opt ? "Boolean" : "boolean";
    default:
      return ts;
  }
};

// ---- emit -------------------------------------------------------------------

const header = `// GENERATED by scripts/gen-harness-prompts.mjs from the manabrew protocol. Do not edit.\npackage ${PKG};\n`;
const files = new Map();
const emit = (name, body) => files.set(name, `${header}\n${body}\n`);

const constName = (v) =>
  v
    .replace(/([a-z0-9])([A-Z])/g, "$1_$2")
    .toUpperCase()
    .replace(/[^A-Z0-9]/g, "_");

const emitStringEnum = (name, body) => {
  const consts = body
    .split("|")
    .map((v) => v.trim().replace(/"/g, ""))
    .map((v) => `  @com.google.gson.annotations.SerializedName("${v}")\n  ${constName(v)}`)
    .join(",\n");
  emit(name, `public enum ${name} {\n${consts};\n}`);
};

const parentOf = new Map(); // variant class name -> interface it implements

const emitClass = (name, discriminator, fields, tag) => {
  const baked = tag ? { key: "type", value: tag } : discriminator;
  const decls = fields.map((f) => `  public ${javaType(f.ts, f.optional)} ${f.name};`);
  const params = fields.map((f) => `${javaType(f.ts, f.optional)} ${f.name}`).join(", ");
  const assigns = fields.map((f) => `    this.${f.name} = ${f.name};`);
  const discDecl = baked ? `  public final String ${baked.key} = "${baked.value}";\n` : "";
  const impl = parentOf.has(name) ? ` implements ${parentOf.get(name)}` : "";
  const allArgsCtor = `  public ${name}(${params}) {\n${assigns.join("\n")}\n  }`;
  // All-args-only by default, so forgetting a required field is a compile error.
  // The no-arg constructor is added only for zero-field classes (e.g. gameOver)
  // and large DTOs built field-by-field (CardDto — a ~50-arg positional call is
  // transposition-prone). Those large DTOs trade the missing-field check for it.
  let ctors;
  if (fields.length === 0) ctors = `  public ${name}() {}`;
  else if (fields.length > 15) ctors = `  public ${name}() {}\n\n${allArgsCtor}`;
  else ctors = allArgsCtor;
  emit(name, `public final class ${name}${impl} {\n${discDecl}${decls.join("\n")}\n\n${ctors}\n}`);
};

// An object union, optionally `{ common } & ( v1 | v2 | … )`; null if not a union.
const parseUnion = (body) => {
  let commonFields = [];
  let unionBody = body;
  const inter = body.match(/^(\{.*?\}) & \((.+)\)$/);
  if (inter) {
    commonFields = parseFields(inter[1]).fields;
    unionBody = inter[2];
  }
  const parts = splitTopLevel(unionBody, " | ");
  if (parts.length < 2 || !parts.every((p) => p.startsWith("{"))) return null;
  return { commonFields, parts };
};

// first pass: register union variants so they can `implements` their interface
const unions = new Map();
for (const name of reachable) {
  const u = parseUnion(aliases.get(name));
  if (!u) continue;
  const variants = u.parts.map((p, i) => {
    const inter = p.match(/^(\{.*\}) & (\w+)$/);
    const objPart = inter ? inter[1] : p;
    const refFields = inter ? parseFields(aliases.get(inter[2])).fields : [];
    const { discriminator, fields } = parseFields(objPart);
    const vName = `${name}_${discriminator ? discriminator.value : i}`;
    parentOf.set(vName, name);
    return { vName, discriminator, fields: [...u.commonFields, ...fields, ...refFields] };
  });
  unions.set(name, variants);
}

for (const name of reachable) {
  const body = aliases.get(name);
  if (unions.has(name)) {
    emit(name, `public interface ${name} {}`);
    for (const v of unions.get(name)) emitClass(v.vName, v.discriminator, v.fields, null);
  } else if (/^"/.test(body)) {
    emitStringEnum(name, body);
  } else {
    const { discriminator, fields } = parseFields(body);
    emitClass(name, discriminator, fields, inputTag.get(name) ?? null);
  }
}

rmSync(outDir, { recursive: true, force: true });
mkdirSync(outDir, { recursive: true });
for (const [name, src] of files) writeFileSync(join(outDir, `${name}.java`), src);
console.log(
  `generated ${files.size} java classes (${reachable.size} protocol types) into ${outDir.replace(root + "/", "")}`,
);
