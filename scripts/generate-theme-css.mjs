#!/usr/bin/env node
/**
 * Auto-generate the game-theme `@theme` CSS block for Tailwind v4.
 *
 * Reads the default preset's gameColors keys (the canonical token set),
 * converts each dot-notation path to the kebab-case CSS variable name
 * that `flattenGameThemeToCssVars()` emits, and outputs the `@theme`
 * entries. The output replaces the hand-maintained game-token section
 * in `src/index.css`.
 *
 * Usage:
 *   node scripts/generate-theme-css.mjs          # print to stdout
 *   node scripts/generate-theme-css.mjs --write   # update index.css in place
 */

import { readFileSync, writeFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");

// ---------------------------------------------------------------------------
// 1. Extract dot-notation keys from buildGameColors.ts via regex
//    (avoids needing tsx/ts-node to import TypeScript at script time)
// ---------------------------------------------------------------------------

const buildSrc = readFileSync(
  resolve(ROOT, "src/themes/buildGameColors.ts"),
  "utf-8",
);

// Match all string keys in the return object: "activeAction.priority", "mana.W", etc.
const keyRegex = /^\s*"([a-zA-Z]+(?:\.[a-zA-Z0-9]+)*)"\s*:/gm;
const keys = [];
let match;
while ((match = keyRegex.exec(buildSrc)) !== null) {
  keys.push(match[1]);
}

if (keys.length === 0) {
  console.error("ERROR: No game-theme keys found in buildGameColors.ts");
  process.exit(1);
}

// ---------------------------------------------------------------------------
// 2. Convert dot-notation to kebab-case CSS var names
//    Must match flattenGameThemeToCssVars() in gameTheme.ts
// ---------------------------------------------------------------------------

function camelToKebab(s) {
  return s.replace(/[A-Z]/g, (ch, i) =>
    i === 0 ? ch.toLowerCase() : `-${ch.toLowerCase()}`,
  );
}

function dotPathToCssVar(dotPath) {
  return dotPath
    .split(".")
    .map((seg) => camelToKebab(seg).toLowerCase())
    .join("-");
}

const cssLines = keys.map((key) => {
  const varName = dotPathToCssVar(key);
  return `  --color-${varName}: var(--${varName});`;
});

const block = [
  "  /* ── Game theme tokens (auto-generated by scripts/generate-theme-css.mjs) ── */",
  ...cssLines,
].join("\n");

// ---------------------------------------------------------------------------
// 3. Output or patch index.css
// ---------------------------------------------------------------------------

const indexPath = resolve(ROOT, "src/index.css");

if (process.argv.includes("--write")) {
  const css = readFileSync(indexPath, "utf-8");

  // Find the block between the game-theme comment and the next non-game-token
  // line (--radius-lg or similar). The generated block replaces everything
  // from the comment through the last --color-* line before that boundary.
  // Match either the auto-generated single-line comment or the old multi-line comment
  const startMatch =
    css.match(/\s*\/\* ── Game theme tokens \(auto-generated[^]*?\*\/\n/) ||
    css.match(/\s*\/\*[\s─]*\n\s*\*\s*Game theme tokens[^]*?\*\/\n/);

  if (!startMatch) {
    console.error("ERROR: Could not find game-theme start marker in index.css");
    process.exit(1);
  }

  const startIdx = startMatch.index;

  // Find the end: last --color-* line before the next non-color declaration
  const after = css.slice(startIdx);
  const endMatch = after.match(/--color-[\w-]+:[^;]*;\n(?=\n\s*--(?!color))/);
  if (!endMatch) {
    console.error("ERROR: Could not find game-theme end boundary in index.css");
    process.exit(1);
  }

  const endIdx = startIdx + endMatch.index + endMatch[0].length;
  const patched = css.slice(0, startIdx) + "\n  " + block.trimStart() + "\n" + css.slice(endIdx);

  writeFileSync(indexPath, patched);
  console.log(`✓ Updated ${keys.length} game-theme tokens in src/index.css`);
} else {
  console.log(`Found ${keys.length} game-theme tokens:\n`);
  console.log(block);
  console.log("\nRun with --write to update src/index.css");
}
